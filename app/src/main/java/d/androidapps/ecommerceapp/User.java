package d.androidapps.ecommerceapp;

public class User {
    private long phone;
    private String password;
    private String name;
    private String email;
    private int earning;

    public long getPhone() {
        return phone;
    }

    public String getPassword() {
        return password;
    }

    public String getName() {
        return name;
    }

    public String getEmail() {
        return email;
    }

    public int getEarning() {
        return earning;
    }
}
